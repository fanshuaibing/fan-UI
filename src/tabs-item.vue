<template>
  <div class="tabs-item" @click="onClick" :class="classes">
    <slot></slot>
  </div>
</template>

<script>
  export default {
    name:"fanTabsItem",
    inject: ['eventBus'],
    data () {
      return {
        active: false
      }
    },
    props:{
      disabled: {
        type: Boolean,
        default: false
      },
      name: {
        type: String | Number,
        required: true
      },
    },

    computed: {
      classes () {
        return {
          active: this.active,
          disabled:this.disabled
        }
      }
    },
    created () {
      if(this.eventBus){
        this.eventBus.$on('update:selected', (name) => {
          this.active = name === this.name;
        })
      }

    },
    methods: {
      onClick () {
        if (this.disabled) { return }
        if(this.eventBus){
          this.eventBus.$emit('update:selected', this.name,this)
        }
      }
    }

  }
</script>

<style scoped lang="scss">
  $blue: #00ccff;
  $disabled-text-color: gray;

  .tabs-item {
    flex-shrink: 0;
    padding: 0 1em;
    cursor: pointer;
    height: 100%;
    display: flex;
    align-items: center;
    &.active {
      color: $blue;
      font-weight: bolder;
    }
    &.disabled{
      color: $disabled-text-color;
      cursor: not-allowed;
    }
  }
</style>